#Defining the dictionary
dict_num = {1: 10, 2: 20, 3: 30, 4: 40, 5: 50, 6: 60}

#Getting the keys from the dictionary
keys = dict_num.keys()

#Getting the values from the dictionary
values = dict_num.values()

#Getting the items (key-value pairs) from the dictionary
items = dict_num.items()

#Printing the keys, values, and items
print("Keys in the dictionary:", list(keys))
print("Values in the dictionary:", list(values))
print("Items in the dictionary:", list(items))
